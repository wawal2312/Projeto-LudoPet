<h4>
    Somos estudantes do curso de Engenharia da Computação, movidos pelo desejo de aplicar a tecnologia para
transformar vidas de maneira positiva. Nosso objetivo com este projeto é ajudar pais, mães e familiares de pessoas
autistas a acessarem informações confiáveis, atualizadas e organizadas sobre clínicas, tratamentos, terapias,
profissionais e outros recursos disponíveis em São Luís – MA.
<br>
<br>
Sabemos que o diagnóstico do Transtorno do Espectro Autista (TEA) pode trazer muitas dúvidas, inseguranças e
desafios para as famílias. Acreditamos que a informação de qualidade é uma das principais formas de apoio, por isso
nos dedicamos a criar uma plataforma digital que facilite o acesso a esses dados de forma clara, simples e inclusiva.
<br>
Queremos que este site seja um ponto de partida, um canal de apoio e orientação. Um lugar onde mães, pais e
responsáveis encontrem suporte, acolhimento e caminhos possíveis para cuidar, entender e acompanhar seus filhos
autistas com mais confiança e tranquilidade.
<br>   
Nosso compromisso é com o respeito à neurodiversidade, à inclusão e à construção de uma sociedade mais empática e
informada. 
