<h1>
    O que é o Transtorno do Espectro Autista (TEA)?
</h1>
<h4>
    O Transtorno do Espectro Autista (TEA) é uma condição neurológica e do desenvolvimento que acompanha a pessoa por toda a vida. Ele
afeta principalmente a comunicação, a interação social, o comportamento e a forma como o indivíduo percebe o ambiente ao seu redor.
O termo "espectro" é utilizado porque existem diferentes formas e graus de manifestação do autismo, que variam de pessoa para
pessoa.
</h4>
<h2>
    Pessoas com TEA podem apresentar:
</h2>
<h4>
-Dificuldade em iniciar ou manter interações sociais.

-Atrasos na fala ou ausência de linguagem verbal.

-Interesses restritos e comportamentos repetitivos.

-Resistência a mudanças na rotina.

-Reações intensas a estímulos sensoriais, como sons, luzes ou texturas.


Apesar desses desafios, muitas pessoas autistas têm habilidades únicas e podem se destacar em áreas como lógica, arte, memorização,
tecnologia e outras, especialmente quando recebem suporte adequado.
É importante lembrar: o autismo não é uma doença. Ele não tem cura, mas tem tratamento, acompanhamento e acolhimento que fazem
toda a diferença no desenvolvimento e qualidade de vida da pessoa autista.
</h4>
